import Foundation
import UIKit

extension UIFont {
    static func nunito(size: CGFloat) -> UIFont? {
        UIFont(name: "Nunito", size: size)
    }
    
    static func orange(size: CGFloat) -> UIFont? {
        UIFont(name: "Orange Slices", size: size)
    }
}
